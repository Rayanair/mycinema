    <h1>My_cinema</h1>
    <p>
my_cinema est un projet en PHP et MySQL c'est un site web pour afficher les films et membres de de la base de donnée my_cinema. 
Il est possible de faire une recherche par titre, date, genre et nom de distributeur pour les films.
Il est également possible d'afficher les abonnés du cinéma, 
ajouter, supprimer ou modifier leurs abonnements.
</p>
<h2>Utilisation</h2>
<p>
Depuis la page d'accueil nous pouvons accéder à la recherche de films et ainsi utiliser le formulaire de recherche pour chercher des films par titre, date, genre et nom de distributeur.
Nous pouvons aussi voirs les scéance du film choisis et lui ajouter une scéance.
Accédez à la page Membres pour afficher les abonnés du cinema et leurs information grace au bouton "Voir" l'historique des films visionner est afficher et le bouton ajouter film permet d'integrer un film à leurs historique.
Depuis la page administration nous pouvons supprimer ou modifier l'abonnement d'un membre.
La page client elle, permet l'ajout d'un abonnement à un non membre.
Et pour finir la page employe permet la visualisation des employés du cinema ainsi que leurs poste et sa description</p>
    
